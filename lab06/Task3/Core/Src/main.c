{
  uint16_t len = 0;
  
  // Format the data for transmission
  len = sprintf((char*)uart_buffer, 
                "Frequency: %.2f Hz, RPM: %.2f\r\n", 
                frequency_hz, rpm);
  
  // Send data via UART1
  HAL_UART_Transmit(&huart1, uart_buffer, len, 100);
}

